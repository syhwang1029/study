# 091 dictionary 생성
# 딕셔너리 변수명 = {이름:[가격,재고]} 
# {}는 딕셔너리, []는 리스트
inventory = {
            '메로나':[300, 200], #풀이
            '비비빅':[400, 3],
            '죠스바':[250, 100]
             }
print(inventory)

# 풀이 : 
# inventory = {'메로나':[300, 200]} 는
# inventory 라는 딕셔너리에 
# '메로나' 라는 key와 리스트 형식의 300, 200 value 데이터를 저장함.

# 결과 : {'메로나': [300, 200], '비비빅': [400, 3], '죠스바': [250, 100]}

