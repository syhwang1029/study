# 036 문자열의 format( ) 메서드
name36_1 = "김민수" 
age36_1 = 10
name36_2 = "이철희"
age36_2 = 13
print("이름 : {} 나이: {} ".format(name36_1, age36_1))
print("이름 : {} 나이: {} ".format(name36_2, age36_2))
# 풀이 : format는 {}으로 글자의 위치를 정하고, format() 안에 변수를 지정함으로써 문자열 포맷이 가능함.
# 결과물 :
# 이름 : 김민수 나이: 10
# 이름 : 이철희 나이: 13