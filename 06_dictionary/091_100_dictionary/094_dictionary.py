# 094 dictionary 추가
inventory = {"메로나": [300, 20],
              "비비빅": [400, 3],
              "죠스바": [250, 100]}
inventory["월드콘"] = [500,7]
print(inventory)

# 풀이 : inventory 딕셔너리에 key인 "월드콘"과 value인 리스트[500,7] 라는 새데이터를 추가함.
# 결과 : {'메로나': [300, 20], '비비빅': [400, 3], '죠스바': [250, 100], '월드콘': [500, 7]}