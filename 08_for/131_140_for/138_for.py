# 위 같은 결과 나오는 for문 작성

# num 변수에 숫자요소 할당
for num in [10,20,30]:
    # num값 반복
    # 리스트는 한요소당 한라인씩 출력
    print(num)
    # 문자열 반복
    print("------")
# 결과 : 
# 10
# ------
# 20
# ------
# 30
