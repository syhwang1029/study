# 194 for * 답안 확인

# 숫자 리스트
data = [[2000,  3050,  2050,  1980],
        [7500,  2050,  2050,  1980],
        [15450, 15050, 15550, 14900]]
# 2차원 리스트
# 초기화
result = []

# a 변수에 리스트 저장
for a in data:
    # [2000, 3050, 2050, 1980]
    #print(a)

    # b 변수에 요소 분할 후 저장
    for b in a:
        # 2000
        # 3050
        # 2050
        # print(b)

        # 초기화
        c = []
        
        # 연산 후 한 요소씩 리스트에 저장
        c.append(b * 1.00014)
        # [2000.28]
        # [3050.427]
        # [2050.2870000000003]
        # print(c)

        # 2차원 배열로 저장
        result.append(c)
    # 결과값
    print(result)

